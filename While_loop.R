# WHILE LOOP
# put a logical expression in while lets say abc here
# In curly braces put the body of loop here lets say xyz
# It will keep on repeating loop until abc becomes true
# loops are basically for iterating certain operations

while(abc){
  xyz
}

# does not execute even once since always FALSE
while(FALSE){
  print("Hello")
}

# executes infinite since always TRUE
while(TRUE){
  print("Hello")
}

# counter loop
counter <- 1
while (counter < 12){
  print(counter)
  counter <- counter + 1
}

