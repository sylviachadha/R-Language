# for loop 
# simple braces instead of specifying the logical expression it specifies
# the iterations of the loop curly brackets specify the body of loop

# This is a vector
1:5

# prints Hello 5 times 1,2,3,4,5
for (i in 1:5){
  print ("Hello")
}

# prints Hello 6 times 5,6,7,8,9,10
for (i in 5:10){
  print("Hello")
}



